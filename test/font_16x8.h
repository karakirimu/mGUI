class font_16x8 : public mgui_font_property {
public:
    font_16x8(): mgui_font_property() {
        set_font_width(8);
        set_font_height(16);
        set_resource(font);
    }
    virtual ~font_16x8() {}

    int search(const char *c){
        int buf_size = (font_height() >> 3) * font_width();
        int index = 0;
        for (char i = ' '; i <= '~'; i++) {
            if(i == *c){
                return index;
            }
            index += buf_size;
        }
        return -1;
    }
};